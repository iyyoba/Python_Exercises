# Exercise 2
Cabin_class = input("Enter a Cabin Class: ")
if Cabin_class == "A":
    print("Above the car deck, equipped with window.")
elif Cabin_class == "B":
    print("Windowless cabin above the car deck.")
elif Cabin_class == "C":
    print("Windowless cabin below the car deck.")
elif Cabin_class == "LUX":
    print("Upper_deck cabin with a balcony")
else:
    print("Invalid Cabin Class.")
